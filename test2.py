import streamlit as st

st.title("🔮 โปรแกรมทำนายเลขนำโชคสุดแม่น")

# ส่วนที่ 1: เก็บข้อมูลคำถาม 3-4 ข้อ
name = st.text_input("1. คุณชื่ออะไรครับ?")

color = st.selectbox(
    "2. สีที่ชอบที่สุดในช่วงนี้คือสีอะไร?",
    ["สีแดง (พลังงาน)", "สีน้ำเงิน (ความสงบ)", "สีเขียว (ความมั่งคั่ง)", "สีเหลือง (ความสุข)"]
)

mood = st.radio(
    "3. วันนี้คุณรู้สึกอย่างไรบ้าง?",
    ["ดีมาก พร้อมลุย!", "เรื่อยๆ สบายๆ", "เหนื่อยหนิดหน่อย"]
)

number = st.number_input("4. ขอเลขนำโชคที่คุณนึกออกตอนนี้ 1 ตัว:", step=1, min_value=0)

# ส่วนที่ 2: ประมวลผลและสรุป
if st.button("คำนวณโชคชะตา"):
    if name:
        # คำนวณเลขมงคล (ตัวอย่าง logic ง่ายๆ คือเลขที่กรอกมาคูณด้วย 9)
        lucky_result = number * 9
        
        st.divider() # เส้นคั่นเพื่อความสวยงาม
        
        # ข้อความสรุป
        st.subheader(f"🌟 ผลทำนายสำหรับคุณ {name}")
        st.write(f"วิเคราะห์จากสี **{color}** และความรู้สึกที่ว่า **'{mood}'**")
        st.success(f"เลขนำโชคของคุณในวันนี้คือ: {lucky_result}")
        st.info("💡 คำแนะนำ: วันนี้เป็นวันดีที่จะเริ่มทำสิ่งใหม่ๆ ขอให้โชคดีครับ!")
    else:
        st.warning("กรุณาใส่ชื่อก่อนกดคำนวณนะครับ")
