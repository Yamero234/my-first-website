import streamlit as st

st.title("🏀โปรแกรมวัดค่าพลังบาส")

# ส่วนที่ 1: เก็บข้อมูลคำถาม 3-4 ข้อ
name = st.text_input("1. คุณชื่ออะไรครับไก่?")

color = st.selectbox(
    "2. สไตล์การเล่น",
    ["แอ็ค (กระเทย)", "แอ็ค (เกย์)", "แอ็ค (ระรึน)", "แอ็ค (สรัน)"]
)

mood = st.radio(
    "3. คุณคิดว่าคุณเล่นเก่งแค่ไหน?",
    ["เก่งกว่าแก้วกล้า", "ลงไปยืนให้ทีมครบเฉยๆ", "กากเกินเลิกเล่นดีกว่า"]
)

number = st.number_input("4. ขอเลขนำโชคที่คุณนึกออกตอนนี้ 1 ตัว:", step=1, min_value=0)

# ส่วนที่ 2: ประมวลผลและสรุป
if st.button("คำนวณค่าพลัง"):
    if name:
        # คำนวณเลขมงคล (ตัวอย่าง logic ง่ายๆ คือเลขที่กรอกมาคูณด้วย 9)
        lucky_result = number * 9
        
        st.divider() # เส้นคั่นเพื่อความสวยงาม
        
        # ข้อความสรุป
        st.subheader(f"🌟 ผลคะแนนของคุณ {name}")
        st.write(f"วิเคราะห์จากสไตล์ **{color}** และความเก่ง **'{mood}'**")
        st.success(f"ค่าความแอ็คของคุณคือ: {lucky_result}")
        st.info("💡 คำแนะนำ: อยากชนะส่งบอลให้อัมรินทร์")
    else:
        st.warning("กรุณาใส่ชื่อก่อนกดคำนวณนะครับไอ่โง่")
